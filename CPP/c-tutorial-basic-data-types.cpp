#include <iostream>
#include <cstdio>
using namespace std;

int main() {
    int i;
    long int li;
    char c;
    float f;
    double d;
    scanf("%d %ld %c %f %lf", &i, &li, &c, &f, &d);
    printf("%d\n", i);
    printf("%ld\n", li);
    printf("%c\n", c);
    printf("%f\n", f);
    printf("%lf\n", d);
    return 0;
}
